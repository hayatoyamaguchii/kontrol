<h2>経緯・こだわった点</h2>
  <h3>作成経緯</h3>
  <p>筋トレをしていた時に、ノートに１食ずつの食事内容、カロリーや、トレーニングの種類と回数を毎日手書きでノートに書いていました。
それってもっと効率化できるなと思ったので、作りました。</p>
  <h3>こだわった点</h3>
  <p>こだわった点は「扱いやすさ」で、直感的に操作できることにしました。
  トレーニング中の震える指でもタップができるように。</>

<h2>機能一覧</h2>
  <h3>記録機能</h3>
    <h4>食事記録</h4>
      マクロ栄養素を記録できる。
    <h4>トレーニング記録</h4>
      重量、回数、種類などを記録できる。
    <h4>体組成記録</h4>
      体重と体脂肪率を記録できる。

  <h3>閲覧機能</h3>
    <h4>カレンダー</h4>
      カレンダーを用いて、各種登録を見直せる。
    <h4>検索</h4>
      日付指定や種目指定など、各種登録を見直せる。

  <h3>ログイン機能</h3>
      メールアドレス認証を用いたログイン認証が行える。testユーザーも用意している。

